package mum.bigdata.car.recommender.model;

/**
 *
 * @author Hiep
 */
public class BaseModel {
	private long id;
	private String name;

	public BaseModel() {
	}

	public BaseModel(long id, String name) {
		this.id = id;
		this.name = name;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
